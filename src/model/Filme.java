
package model;

public class Filme {
    private int id;
    private String nome;
    private int id_locadora;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getId_locadora() {
        return id_locadora;
    }

    public void setId_locadora(int id_locadora) {
        this.id_locadora = id_locadora;
    }
    
    
}
